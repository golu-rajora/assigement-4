//Write a program to print the first 10 even natural numbers IN reverse order
#include<stdio.h>
int main()
{
    int i=20;
    printf(" the first 10 even natural numbers in reverse order:-");
    while(i>=2)
    {
        printf("\n %d",i);
        i--;
        i--;
    }
    getch();
    return 0;

}

