//Write a program to print the first 10 odd natural numbers IN reverse order
#include<stdio.h>
int main()
{
    int i=19;
    printf(" the first 10 odd natural numbers in reverse order:-");
    while(i>=1)
    {
        printf("\n %d",i);
        i--;
        i--;
    }
    getch();
    return 0;

}

